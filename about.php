        // Counter animation
        function animateCounters() {
            const counters = document.querySelectorAll('.stats-counter');
            
            counters.forEach(counter => {
                const target = parseInt(counter.getAttribute('data-target'));
                const increment = target / 100;
                let current = 0;
                
                const updateCounter = () => {
                    if (current < target) {
                        current += increment;
                        counter.textContent = Math.floor(current);
                        requestAnimationFrame(updateCounter);
                    } else {
                        counter.textContent = target;
                        if (target < 100) {
                            counter.textContent += '%';
                        } else if (target === 24) {
                            counter.textContent += 'H';
                        } else {
                            counter.textContent = target.toLocaleString();
                        }
                    }
                };
                
                updateCounter();
            });
        }

        // Intersection Observer for animations
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    if (entry.target.classList.contains('stats-counter')) {
                        animateCounters();
                        observer.unobserve(entry.target);
                    }
                }
            });
        }, observerOptions);

        // Observe stats counters
        document.querySelectorAll('.stats-counter').forEach(counter => {
            observer.observe(counter);
        });

        // Smooth scrolling for anchor links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });

        // Add scroll effect to navigation
        let lastScrollTop = 0;
        window.addEventListener('scroll', function() {
            const nav = document.querySelector('nav');
            const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
            
            if (scrollTop > 100) {
                nav.classList.add('bg-gray-900/95', 'backdrop-blur-md');
            } else {
                nav.classList.remove('bg-gray-900/95', 'backdrop-blur-md');
            }
            
            lastScrollTop = scrollTop;
        });

        // Feature cards hover effect enhancement
        document.querySelectorAll('.feature-card').forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-10px) scale(1.02)';
            });
            
            card.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0) scale(1)';
            });
        });

        // Loading animation
        window.addEventListener('load', function() {
            document.body.classList.add('loaded');
            
            // Add fade-in animation to sections
            const sections = document.querySelectorAll('section');
            sections.forEach((section, index) => {
                section.style.opacity = '0';
                section.style.transform = 'translateY(20px)';
                
                setTimeout(() => {
                    section.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
                    section.style.opacity = '1';
                    section.style.transform = 'translateY(0)';
                }, index * 100);
            });
        });

        // Add structured data tracking for user engagement
        const trackEngagement = (event, data) => {
            if (typeof gtag !== 'undefined') {
                gtag('event', event, data);
            }
            if (typeof fbq !== 'undefined') {
                fbq('track', event, data);
            }
        };

        // Track CTA clicks
        document.querySelectorAll('a[href="/register.php"]').forEach(link => {
            link.addEventListener('click', function() {
                trackEngagement('register_click', {
                    page: 'about',
                    section: this.closest('section')?.id || 'unknown'
                });
            });
        });

        document.querySelectorAll('a[href="/packages.php"]').forEach(link => {
            link.addEventListener('click', function() {
                trackEngagement('view_packages', {
                    page: 'about',
                    source: 'about_page'
                });
            });
        });
